#include "ft_printf.h"

void	put_nbr(int n, int *a)
{
	if (n == -2147483648)
	{
		*a = put_str("-2147483648");
		return ;
	}
	if (n < 0)
	{
		put_char('-');
		(*a)++;
		n = -n;
	}
	if (n < 10)
	{
		put_char(n + 48);
		(*a)++;
	}
	else
	{
		put_nbr(n / 10, a);
		put_char(n % 10 + 48);
		(*a)++;
	}
}
