#include "ft_printf.h"

void	put_unsigned(unsigned int n, int *a)
{
	if (n < 10)
	{
		put_char(n + 48);
		(*a)++;
	}
	else
	{
		put_unsigned(n / 10, a);
		put_char(n % 10 + 48);
		(*a)++;
	}
}
